package org.example.class10;

public class Printer {

    void print(){
        System.out.println("Hello");
    }

    void print(String name){
        System.out.println("Hi "+name);
    }

    void print(int age){
        System.out.println(age);
    }

    void print(double salary){
        System.out.println(salary);
    }

}
