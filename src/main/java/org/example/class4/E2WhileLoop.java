package org.example.class4;

public class E2WhileLoop {
    public static void main(String[] args) {


        int counter = 15;
        System.out.println(counter);
        counter++; // 1
        System.out.println(counter);
        counter++; //2
        System.out.println(counter);
        counter++; //3
        System.out.println(counter);

    }
}
